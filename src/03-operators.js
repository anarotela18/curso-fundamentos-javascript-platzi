// Operadores Artiméticos

// Suma (+)
const suma = 5+3; // 8
console.log(suma);

// Resta (-)
const resta = 5-3;// 2
console.log(resta);

// Multiplicación
const multiplicacion = 5*3; // 15
console.log(multiplicacion);

// Division (/)
const division = 6 / 3; // 2
console.log(division);

// Módulo (%)
const modulo = 5 % 2; // 1
console.log(modulo);

// Exponenciación (**)
const potencia = 2 ** 3; // 8
console.log(potencia);

// Operadores de Asignación
// Asignación con Suma ( += )
let a = 5;
a += 3;
console.log(a); // 8

// Asignación con Resta ( -= )
let b = 10;
b -= 4;
console.log(b); // 6

// Asignación con Multiplicación ( *= )
let c = 6;
c *= 2;
console.log(c) // 12


// Asignación con División ( /= )
let d = 15;
d /= 3;
console.log(d);

// Operadores de incremento y decremento
let contador = 10;
contador++; // Incrmento en 1
console.log(contador); // 1

contador--; // Decremento en 1
console.log(contador);


// Infiniy y -Infinity
console.log( 1 / 0) // Infiniy
console.log(-1 / 0) // -Infinity

// NaN
console.log( 0 / 0 ); // NaN
console.log('hola' * 2); // NaN

